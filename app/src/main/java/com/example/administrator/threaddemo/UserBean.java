package com.example.administrator.threaddemo;

public class UserBean {
    private int imgNew;
    private int imgAns;
    private String ask;
    private String reward;
    private int ansCount;
    private String imgUserPath;
    private String userName;
    private int goods;
    private int comment;
    private String answer;

    public int getImgNew() {
        return imgNew;
    }

    public void setImgNew(int imgNew) {
        this.imgNew = imgNew;
    }

    public int getImgAns() {
        return imgAns;
    }

    public void setImgAns(int imgAns) {
        this.imgAns = imgAns;
    }

    public String getAsk() {
        return ask;
    }

    public void setAsk(String ask) {
        this.ask = ask;
    }

    public String getReward() {
        return reward;
    }

    public void setReward(String reward) {
        this.reward = reward;
    }

    public int getAnsCount() {
        return ansCount;
    }

    public void setAnsCount(int ansCount) {
        this.ansCount = ansCount;
    }

    public String getImgUserPath() {
        return imgUserPath;
    }

    public void setImgUserPath(String imgUserPath) {
        this.imgUserPath = imgUserPath;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public int getGoods() {
        return goods;
    }

    public void setGoods(int goods) {
        this.goods = goods;
    }

    public int getComment() {
        return comment;
    }

    public void setComment(int comment) {
        this.comment = comment;
    }

    public String getAnswer() {
        return answer;
    }

    public void setAnswer(String answer) {
        this.answer = answer;
    }
}
